export async function PopulateNumbers() {
  for (let i = 0; i < projectJSON.length; i++) {
    projectJSON[i].number = await AddTheNumber(projectJSON[i].nameProject);
  }
}

function AddTheNumber(nameProject) {
  for (let i = 0; i < projectJSON.length; i++) {
    if (projectJSON[i].nameProject === nameProject) {
      let paddedNumber = (i + 1).toString().padStart(2, "0"); // Incrément de 1, puis ajoute un zéro devant si nécessaire
      return paddedNumber;
    }
  }
  return "00";
}
